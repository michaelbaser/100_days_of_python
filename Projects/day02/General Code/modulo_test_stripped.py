even_number = int(input("Please enter your even number:\n"))

odd_number = int(input("Please enter your odd number:\n"))

even_result = even_number % 2

odd_result = odd_number % 2

print(f"The result of your even number ({even_number}) % 2 = {even_result}\n"
      f"The result of your odd number ({odd_number}) % 2 = {odd_result}")
